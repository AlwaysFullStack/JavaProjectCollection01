package com.examples;

public class wholeloop01 {

	public static void main(String[] args) {
		// loop variable
		// ending condition
		// increment or decrement of loop variable
		
		// loop variables i, j, k, x, y z
		
		int i;
		i=1;
		
		System.out.println("Current value of i: "+i);
		
		i=i+1;
		System.out.println("Current value of i: "+i);
		
		i++; //i=i+1;
		System.out.println("Current value of i: "+i);
		
		i++; 
		System.out.println("Current value of i: "+i);
		
		i++; 
		System.out.println("Current value of i: "+i);
	}

}
